<template>
  <section>
    <Panel title="Todo">
      <!-- add todo -->
      <div class="input-group">
        <input ref="newTodo" type="text" class="form-control" value=""
          autofocus
          placeholder="What needs to be done?"
          @keyup.enter="addTodo">
        <span class="input-group-btn">
          <btn text="Add" @click="addTodo"></btn>
        </span>
      </div>

      <!-- todo list -->
      <ul class="list-group">
        <li class="list-group-item" v-for="todo in filteredTodos">
          <todo :item="todo"></todo>
        </li>
      </ul>

      <!-- footer -->
      <template slot="footer">
        <span>
          <strong>{{ remaining }}</strong>
          {{ remaining | pluralize('item') }} left
        </span>
        <ol class="breadcrumb">
          <li v-for="(val, key) in filters"
            :class="{ active: visibility === key }">
            <template v-if="visibility === key">{{ key }}</template>
            <template v-else>
              <a href="#" @click.prevent="visibility = key">{{ key }}</a>
            </template>
          </li>
        </ol>
        <btn text="Clear completed"
          @click="clearCompleted"
          :visible="todos.length > remaining">
        </btn>
      </template>
    </Panel>
  </section>
</template>

<script>
import { mapGetters, mapActions } from 'vuex';
import Todo from './Todo';
import Panel from './Panel';
import btn from './Button';

// filter todo list functions
const filters = {
  all: todos => todos,
  active: todos => todos.filter(todo => !todo.done),
  completed: todos => todos.filter(todo => todo.done),
};

export default {
  components: {
    Todo,
    Panel,
    btn,
  },
  data() {
    return {
      visibility: 'all',
      filters,
    };
  },
  computed: {
    ...mapGetters([
      'todos',
    ]),
    filteredTodos() {
      return filters[this.visibility](this.todos);
    },
    remaining() {
      return this.todos.filter(todo => !todo.done).length;
    },
  },
  methods: {
    addTodo() {
      const input = this.$refs.newTodo;
      const text = input.value;

      if (text.trim()) {
        // TODO: callback
        this.$store.dispatch('addTodo', { text });
      }

      input.value = '';
    },
    ...mapActions([
      'clearCompleted',
    ]),
  },
  filters: {
    pluralize(num, word) {
      return num === 1 ? word : `${word}s`;
    },
  },
};
</script>
